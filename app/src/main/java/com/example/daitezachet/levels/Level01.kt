package com.example.daitezachet.levels

import com.example.daitezachet.engine.GameEngine
import com.example.daitezachet.engine.SpikeDir

/**
 * Уровень 1 — «Ключ на платформе».
 *
 * Механики: прыжок на платформу, сбор ключа, кнопка (только с ключом),
 *           боковые шипы на правой стене, выход через дверь.
 *
 * Маршрут:
 *   Спавн → прыгнуть на левый край длинной платформы →
 *   пройти вправо по платформе → взять ключ →
 *   повернуть НАЗАД (боковые шипы мешают прыгнуть к двери напрямую) →
 *   спрыгнуть на пол → нажать кнопку → подойти к двери по полу (ниже шипов).
 *
 *  ╔═══════════════════════════════════╗
 *  ║                              ▶▶   ║  ← боковые шипы (высота платформы)
 *  ║  ─────────────────────[★]────   ║    ← платформа, ключ справа
 *  ║☻                   [btn]      ▶ ║  ← выход внизу (ниже шипов)
 *  ╚═══════════════════════════════════╝
 */
class Level01 : Level() {
    override val number   = 1
    override val hintText = "Найди ключ"

    override fun setup(engine: GameEngine) {
        // Длинная платформа — почти через всю комнату, y=0.68 как в оригинале (досягаемо с пола)
        engine.addPlatform(0.00f, 0.68f, 0.90f, 0.72f)
        engine.addPlatform(0.95f, 0.20f, 0.97f, 1.00f)


        // Ключ на правой части платформы — нужно пройти туда
        engine.placeKey(0.76f, 0.68f)

        // Боковые шипы на правой стене — только на высоте платформы и выше (y2r=0.62).
        // Нельзя прыгнуть с платформы прямо к двери, но пол ниже шипов — проход чистый.
        engine.addSpikesWall(0.5f, 0.2f, xr = 0.90f, dir = SpikeDir.LEFT)

        // Кнопка открывает дверь только если у игрока есть ключ
        engine.onButtonPressed = { e ->
            if (e.player.hasKey) e.door.isOpen = true
        }

        engine.winCondition = { e -> e.door.isOpen && isPlayerAtDoor(e) }
    }
}
