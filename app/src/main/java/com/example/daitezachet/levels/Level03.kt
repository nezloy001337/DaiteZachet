package com.example.daitezachet.levels

import android.graphics.Color
import com.example.daitezachet.engine.GameEngine
import com.example.daitezachet.engine.SpikeDir

/**
 * Уровень 3 — «Обход».
 *
 * ┌────────────────────────────────────────────────────────┐
 * │▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼│  ← потолочные шипы
 * │                                                    ▶▶  │  ← шипы правой стены (верх)
 * │                    ┌────┐    ┌────┐               ▶▶  │
 * │                    │    │    │    │               ▶▶  │
 * │                    │    │[╗] │    │               ▶▶  │  ← кнопка между колоннами
 * │                  ──┴────┴────┴────┴──                  │  ← платформа у основания колонн
 * │                                                        │
 * │     ────────[R]────────             ────────[B]──────  │  ← платформы с ключами
 * │                                                        │
 * │                 /\/\/\/\/\/\/\/\/\/\/\                 │  ← шипы на полу
 * └────────────────────────────────────────────────────────┘
 *
 * Маршрут:
 *   Спавн → прыгнуть на левую платформу → взять красный ключ →
 *   перепрыгнуть на правую платформу → взять синий ключ →
 *   запрыгнуть на платформу между колоннами →
 *   нажать кнопку (только если оба ключа) → дверь открывается →
 *   спрыгнуть вправо на пол → подойти к двери снизу (ниже шипов стены).
 */
class Level03 : Level() {
    override val number   = 3
    override val hintText = "Сначала оба ключа"

    override fun setup(engine: GameEngine) {

        // ── Потолочные шипы (острие вниз) ──────────────────────────────────
        engine.addSpikesAt(0.03f, 0.97f, yr = 0.00f, dir = SpikeDir.DOWN)

        // ── Шипы правой стены (острие влево) — только верхняя часть ────────
        // y2r=0.62: проход к двери по полу остаётся чистым для любого размера экрана.
        engine.addSpikesWall(0.04f, 0.62f, xr = 0.97f, dir = SpikeDir.LEFT)

        // ── Две вертикальные колонны (опущены ниже) ─────────────────────────
        engine.addPlatform(0.35f, 0.40f, 0.37f, 0.65f)   // левая колонна
        engine.addPlatform(0.53f, 0.40f, 0.55f, 0.65f)   // правая колонна

        // ── Горизонтальная платформа у основания колонн ─────────────────────
        // Кнопка стоит на ней, между колоннами.
        engine.addPlatform(0.28f, 0.65f, 0.62f, 0.69f)
        engine.placeButton(xr = 0.44f, yr = 0.65f)

        // ── Левая платформа — красный ключ (ниже и симметрично) ─────────────
        // Центр левой платформы: x=0.16, расстояние от центра экрана = 0.34
        engine.addPlatform(0.06f, 0.76f, 0.26f, 0.80f)
        engine.placeKey(0.16f, 0.76f, id = 1, color = Color.rgb(255, 80, 80))

        // ── Правая платформа — синий ключ (ниже и симметрично) ──────────────
        // Центр правой платформы: x=0.84, расстояние от центра экрана = 0.34
        engine.addPlatform(0.74f, 0.76f, 0.94f, 0.80f)
        engine.placeKey(0.84f, 0.76f, id = 2, color = Color.rgb(80, 140, 255))

        // ── Шипы на полу по центру ──────────────────────────────────────────
        // Слева и справа от шипов — чистый пол для спавна и выхода.
        engine.addSpikesFloor(0.28f, 0.64f)

        // ── Логика кнопки ───────────────────────────────────────────────────
        // Открывает дверь только если собраны оба ключа.
        engine.onButtonPressed = { e ->
            if (e.collectedKeys.size >= 2) e.door.isOpen = true
        }

        engine.winCondition = { e -> e.door.isOpen && isPlayerAtDoor(e) }
    }
}
